'''
Handles data loading and processing for the dashboard application.
'''
class DataHandler:
    def load_data(self):
        # Simulate loading data
        data = [1, 2, 3, 4, 5]
        return data
    def process_data(self, data):
        # Simulate processing data
        processed_data = [x * 2 for x in data]
        return processed_data